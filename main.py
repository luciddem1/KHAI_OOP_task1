def task3():
    print("=====Завдання 3=====\n")
    print("Дано координати двох різних полів шахової дошки x1, y1, x2, y2 (цілі числа, що лежать в діапазоні 1-8). Перевірити істинність висловлювання: «Дані поля мають однаковий колір».\n")
    
    # Перевірка координат першого поля
    while True:
        x1 = int(input("Введіть координату x першого поля (1-8): "))
        if 1 <= x1 <= 8:
            break
        else:
            print(f"Координата {x1} не коректна.")

    while True:
        y1 = int(input("Введіть координату y першого поля (1-8): "))
        if 1 <= y1 <= 8:
            break
        else:
            print(f"Координата {y1} не коректна.")

    # Перевірка координат другого поля
    while True:
        x2 = int(input("Введіть координату x другого поля (1-8): "))
        if 1 <= x2 <= 8:
            break
        else:
            print(f"Координата {x2} не коректна.")

    while True:
        y2 = int(input("Введіть координату y другого поля (1-8): "))
        if 1 <= y2 <= 8:
            break
        else:
            print(f"Координата {y2} не коректна.")

    color1 = (x1 + y1) % 2  # Визначення кольору першого поля
    color2 = (x2 + y2) % 2  # Визначення кольору другого поля

    if color1 == color2:
        print("Дані поля мають однаковий колір.\n")  # Виведення результату перевірки
    else:
        print("Дані поля мають різний колір.\n")

main()  # Виклик головної функції для виконання програми
